# Operator 

z = 0.2241
pi = 3.141592 
# = means input operator (math equal sign is ==)

# + - / * 
print(z+pi) 
print(z-pi) 
print(z/pi) 
print(z*pi) 

# / / %divmod()
print(pi // z)    # (%)--> remainder   
print(pi % z)     # (//) --> quotient 
print(divmod(10,3))

# ** 
print(2**4) # same as 2*2*2*2
 
